     * Transform the resource into an array.
     *
     * @return array<string, mixed>
     */
    public function toArray(Request $request): array
    {
        return parent::toArray($request);
    }
    public function with(Request $request){
        return [
            'code' =>'200' ,
            'status' =>'success' ,
        ];
    }
}
